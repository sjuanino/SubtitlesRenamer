    /** Limpia los JTextFields y las JLabels */
    private void cleanTxtFields() {
        lblProceso.setVisible(false);
        lblFinProcesoSubs.setVisible(false);
        txtDirEpisodes.setText(null);
        txtDirectory.setText(null);
        txtSaveFile.setText(null);
    }
     
    /** Inicia la JList */
    private void initJList() {
        this.jListModelo = new DefaultListModel();
        this.jlFiles.setModel(jListModelo);
    }
            
    /** Inicia la JTable */
    private void initJtable() {
        jTableModelo = new DefaultTableModel();
        jTableModelEp = new DefaultTableModel();
        tblEpisodes.setModel(jTableModelo);
        tblEpisodes.setDefaultEditor(Object.class, null); // Hace que las celdas no sean editables  
        jTableModelo.addColumn("Episodes");
    }
    
    /** Pinta el icono en el jFrame */
    private void initIcon() {
        try {
            URL resource = getClass().getResource("/resources/subtitle32.png");
            BufferedImage image = ImageIO.read(resource);
            this.setIconImage(image);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    /** Elimina los elementos que estaban en la JTable */
    private void clearJTable() {
        DefaultTableModel model = (DefaultTableModel) this.tblEpisodes.getModel();
        model.setRowCount(0);
    }
    
    /** Elimina los elementos que estaban el JList */
    private void clearJList() {
        DefaultListModel listModel = (DefaultListModel) this.jlFiles.getModel();
        listModel.removeAllElements();
    }
        
